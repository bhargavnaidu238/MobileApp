/// ================== AUTH APIs ==================
class ApiService {

  /// ================== REGISTER (WEB) ==================
  static Future<bool> registerUser({
    required String email,
    required String firstName,
    required String lastName,
    required String gender,
    required String mobile,
    required String address,
    required String password,
    required String consent,
  }) async {
    final url = Uri.parse('${ApiConfig.baseUrl}/register');

    final body = jsonEncode({
      'email': email,
      'firstName': firstName,
      'lastName': lastName,
      'gender': gender,
      'mobile': mobile,
      'address': address,
      'password': password,
      'consent': consent,
    });

    try {
      final response = await http.post(
        url,
        headers: {'Content-Type': 'application/json'},
        body: body,
      );
      return response.statusCode == 200;
    } catch (e) {
      print('‚ùå Register Error: $e');
      return false;
    }
  }

  /// ================== LOGIN (WEB) ==================
  static Future<Map<String, dynamic>?> loginUser({
    required String email,
    required String password,
  }) async {
    final url = Uri.parse('${ApiConfig.baseUrl}/login');
    final body = jsonEncode({'email': email, 'password': password});

    try {
      final response = await http.post(
        url,
        headers: {'Content-Type': 'application/json'},
        body: body,
      );

      final data = jsonDecode(response.body);
      print("üì© Login Response ‚Üí $data");

      if (data.containsKey("error")) return data;

      if (response.statusCode == 200) {
        return {
          'email': data['email'] ?? email,
          'userId': data['userId'] ?? '',
          ...data,
        };
      }

      return {"error": "server_error"};
    } catch (e) {
      print('üö® Login Error: $e');
      return {"error": "connection_error"};
    }
  }

  /* ============================================================
     üîê FORGOT PASSWORD (APP ONLY)
     ============================================================ */

  static Future<Map<String, dynamic>> verifyForgotPassword({
    required String email,
    required String mobile,
  }) async {
    final url =
    Uri.parse('${ApiConfig.baseUrl}/app/forgot-password/verify');

    try {
      final response = await http.post(
        url,
        headers: {"Content-Type": "application/json"},
        body: jsonEncode({
          "email": email.trim(),
          "mobile": mobile.trim(),
        }),
      );

      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        return {"matched": data['matched'] == true};
      }
    } catch (e) {
      print('‚ùå Verify Forgot Password Error: $e');
    }
    return {"matched": false};
  }

  static Future<Map<String, dynamic>> changePassword({
    required String email,
    required String newPassword,
  }) async {
    final url =
    Uri.parse('${ApiConfig.baseUrl}/app/forgot-password/change');

    try {
      final response = await http.post(
        url,
        headers: {"Content-Type": "application/json"},
        body: jsonEncode({
          "email": email.trim(),
          "newPassword": newPassword,
        }),
      );

      return {"success": response.statusCode == 200};
    } catch (e) {
      print('‚ùå Change Password Error: $e');
      return {"success": false};
    }
  }

  /* ============================================================
     üîê CHANGE PASSWORD (PROFILE ‚Äì WITH CURRENT PASSWORD)
     ============================================================ */

  static Future<bool> changePasswordWithCurrent({
    required String email,
    required String currentPassword,
    required String newPassword,
  }) async {
    final url = Uri.parse('${ApiConfig.baseUrl}/app/change-password');

    try {
      final response = await http.post(
        url,
        headers: {"Content-Type": "application/json"},
        body: jsonEncode({
          "email": email.trim(),
          "currentPassword": currentPassword,
          "newPassword": newPassword,
        }),
      );

      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        return data['success'] == true;
      }
    } catch (e) {
      print('‚ùå Change Password (Profile) Error: $e');
    }
    return false;
  }
}

/// ================== PROFILE APIs ==================
class ProfileApiService {

  /// üîπ Fetch Profile
  static Future<Map<String, dynamic>?> fetchProfile({
    required String email,
  }) async {
    final url = Uri.parse(
      '${ApiConfig.baseUrl}/profile?email=${Uri.encodeComponent(email)}',
    );

    try {
      final response = await http.get(url);
      if (response.statusCode == 200 && response.body.isNotEmpty) {
        final data = jsonDecode(response.body);
        return {
          "userId": data['userId']?.toString() ?? '',
          "email": data['email'] ?? email,
          "firstName": data['firstName'] ?? '',
          "lastName": data['lastName'] ?? '',
          "phone": data['phone'] ?? '',
          "address": data['address'] ?? '',
        };
      }
    } catch (e) {
      print('üö® FetchProfile Error: $e');
    }
    return null;
  }

  /// üîπ Update Profile
  static Future<bool> updateProfile({
    required String email,
    required String userId,
    required String firstName,
    required String lastName,
    required String phone,
    required String address,
  }) async {
    final url = Uri.parse('${ApiConfig.baseUrl}/profile');

    try {
      final response = await http.post(
        url,
        headers: {"Content-Type": "application/json"},
        body: jsonEncode({
          "email": email,
          "userId": userId,
          "firstName": firstName,
          "lastName": lastName,
          "phone": phone,
          "address": address,
        }),
      );
      return response.statusCode == 200;
    } catch (e) {
      print('üö® UpdateProfile Error: $e');
      return false;
    }
  }

  /// üîπ Deactivate Account
  static Future<bool> deactivateAccount({
    required String email,
    required String userId,
    required String status,
  }) async {
    final url = Uri.parse('${ApiConfig.baseUrl}/profile');

    try {
      final response = await http.post(
        url,
        headers: {"Content-Type": "application/json"},
        body: jsonEncode({
          "email": email.trim(),
          "userId": userId.trim(),
          "status": status.trim(),
        }),
      );
      return response.statusCode == 200;
    } catch (e) {
      print('‚ùå Deactivate Error: $e');
      return false;
    }
  }
}
